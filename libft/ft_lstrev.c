#include "libft.h"

void	ft_lstrev(t_list **lst)
{
	t_list	*current;
	t_list	*next;
	t_list	*previous;

	current = *lst;
	previous = NULL;
	while (current != NULL)
	{
		next = current->next;
		current->next = previous;
		previous = current;
		current = next;
	}
	*lst = previous;
}
