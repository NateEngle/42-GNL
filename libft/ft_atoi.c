#include "libft.h"

int			ft_atoi(char *s)
{
	int		i;
	int		neg;
	long	value;

	i = 0;
	value = 0;
	neg = 1;
	while (s[i] == ' ' || s[i] == '\t' || (s[i] <= 13 && s[i] >= 9))
		i++;
	while (!(s[i] >= '0' && s[i] <= '9'))
	{
		if (s[i] == '-' && (s[i + 1] >= '0' && s[i + 1] <= '9'))
			neg = -1;
		else if (s[i] == '+' && (s[i + 1] >= '0' && s[i + 1] <= '9'))
			neg = 1;
		else if (!(s[i] == ' ' || s[i] == '\t' || (s[i] <= 13 && s[i] >= 9)))
			return (0);
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		value = value * 10 + (s[i] - '0');
		i++;
	}
	return (value * neg);
}
